    public function configureDashboard(): Dashboard
    {
        return Dashboard::new()
            ->setTitle('Espadrille Volante');
    }

    public function configureMenuItems(): iterable
    {
        yield MenuItem::linkToDashboard('Tableau de bord', 'fa fa-home');
        yield MenuItem::linkToCrud('Les Hébergements', 'fas fa-list', Logement::class);
        yield MenuItem::linkToCrud('Les Utilisateurs', 'fas fa-list', User::class);
        yield MenuItem::linkToCrud('Les Réservations', 'fas fa-list', Reservation::class);
        yield MenuItem::linkToCrud('Info Logements', 'fas fa-list', InfoLogement::class);
